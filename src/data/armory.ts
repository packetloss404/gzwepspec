export type StatKey =
  | "ergonomics"
  | "recoil"
  | "accuracy"
  | "weight"
  | "velocity"
  | "ads";

export type Stats = Record<StatKey, number>;

export type CatalogPrice = {
  amount: number;
  currency: "USD" | "LRI";
  barter?: string;
};

export type UnlockInfo = {
  vendor: string;
  level: 1 | 2 | 3;
  task?: string;
  reputation?: "neutral" | "friendly" | "trusted";
};

export type Finish = {
  id: string;
  name: string;
  color: string;
  material?: "polymer" | "anodized" | "phosphate" | "blued" | "wood" | "paint";
  unlock?: UnlockInfo;
  price?: CatalogPrice;
  tags?: string[];
};

export type Slot =
  | "receiver"
  | "barrel"
  | "handguard"
  | "muzzle"
  | "stock"
  | "pistolGrip"
  | "magazine"
  | "opticMount"
  | "optic"
  | "foregrip"
  | "tactical"
  | "laser"
  | "underbarrelAdapter"
  | "sideRailAdapter";

